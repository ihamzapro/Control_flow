// General syntax to write a switch
const month = 3;
switch (month) {
  case 1: //We can also use string in comparision
    console.log("January");
    break;
  case 2:
    console.log("Feb");
    break;
  case 3:
    console.log("March");
    break;
  case 4:
    console.log("April");
    break;
  case 5:
    console.log("May");
    break;

  default:
    console.log("Default case match");
    break;
}
